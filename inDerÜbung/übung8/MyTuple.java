// https://en.wikipedia.org/wiki/Generics_in_Java

public class MyTuple<A, B, C> {
	
	public void setA(A a) {
		this.a = a;
	}
	public void setB(B b) {
		this.b = b;
	}
	public void setC(C c) {
		this.c = c;
	}
	
	public A getA() {
		return a;
	}
	public B getB() {
		return b;
	}
	public C getC() {
		return c;
	}
	public String toString() {
			return ("(" + a.toString() + ", " + b.toString() + ", " + c.toString() + ")");
	}
	
	A a;
	B b;
	C c;
}
